// ─── Summary Excel ───────────────────────────────────────────────────────────

export async function exportSummaryToExcel(
  rows: AttendanceSummary[],
  periodLabel: string,
  className: string
) {
  const XLSX = await import("xlsx");

  const data = [
    [`${periodLabel} Attendance Summary — ${className}`],
    [`Printed: ${new Date().toLocaleString("en-PH")}`],
    [],
    [periodLabel, "Sessions", "Present", "Absent"],
    ...rows.map((r) => [r.period, r.total_sessions, r.present_count, r.absent_count]),
  ];

  const wb = XLSX.utils.book_new();
  const ws = XLSX.utils.aoa_to_sheet(data);
  ws["!cols"] = [{ wch: 20 }, { wch: 12 }, { wch: 12 }, { wch: 12 }];
  XLSX.utils.book_append_sheet(wb, ws, `${periodLabel} Summary`);

  const fileName = `${className.replace(/\s+/g, "_")}_${periodLabel.toLowerCase()}_summary.xlsx`;
  XLSX.writeFile(wb, fileName);
}

