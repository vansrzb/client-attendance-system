// ─── Excel via SheetJS ───────────────────────────────────────────────────────

export async function exportToExcel(
  session: AttendanceSession,
  records: AttendanceRecord[],
  className: string
) {
  const XLSX = await import("xlsx");

  const present = records.filter((r) => r.status === "present").length;
  const absent = records.filter((r) => r.status === "absent").length;

  // Summary sheet
  const summaryData = [
    ["Attendance Report"],
    ["Class", className],
    ["Date", formatDate(session.attendance_date)],
    [""],
    ["Present", present],
    ["Absent", absent],
    ["Total", records.length],
  ];

  // Records sheet
  const recordsData = [
    ["#", "Student No.", "Full Name", "Status", "Scanned At"],
    ...records.map((r, i) => [
      i + 1,
      r.student_number,
      r.full_name,
      r.status.charAt(0).toUpperCase() + r.status.slice(1),
      formatTime(r.scanned_at),
    ]),
  ];

  const wb = XLSX.utils.book_new();

  // Summary sheet
  const wsSummary = XLSX.utils.aoa_to_sheet(summaryData);
  wsSummary["!cols"] = [{ wch: 18 }, { wch: 40 }];
  XLSX.utils.book_append_sheet(wb, wsSummary, "Summary");

  // Records sheet
  const wsRecords = XLSX.utils.aoa_to_sheet(recordsData);
  wsRecords["!cols"] = [
    { wch: 4 },
    { wch: 16 },
    { wch: 36 },
    { wch: 12 },
    { wch: 14 },
  ];
  XLSX.utils.book_append_sheet(wb, wsRecords, "Records");

  const fileName = `attendance_${className.replace(/\s+/g, "_")}_${session.attendance_date}.xlsx`;
  XLSX.writeFile(wb, fileName);
}

// ─── Word via docx ───────────────────────────────────────────────────────────

export async function exportToWord(
  session: AttendanceSession,
  records: AttendanceRecord[],
  className: string
) {
  const {
    Document,
    Packer,
    Paragraph,
    Table,
    TableRow,
    TableCell,
    TextRun,
    HeadingLevel,
    AlignmentType,
    BorderStyle,
    WidthType,
    ShadingType,
  } = await import("docx");

  const present = records.filter((r) => r.status === "present").length;
  const absent = records.filter((r) => r.status === "absent").length;

  const headerCellStyle = (text: string) =>
    new TableCell({
      shading: { type: ShadingType.SOLID, color: "166534", fill: "166534" },
      borders: {
        top: { style: BorderStyle.NONE },
        bottom: { style: BorderStyle.NONE },
        left: { style: BorderStyle.NONE },
        right: { style: BorderStyle.NONE },
      },
      children: [
        new Paragraph({
          children: [new TextRun({ text, bold: true, color: "FFFFFF", size: 20 })],
          alignment: AlignmentType.CENTER,
        }),
      ],
    });

  const dataCell = (text: string, highlight?: boolean) =>
    new TableCell({
      shading: highlight ? { type: ShadingType.SOLID, color: "F0FDF4", fill: "F0FDF4" } : undefined,
      borders: {
        top: { style: BorderStyle.SINGLE, size: 1, color: "E5E7EB" },
        bottom: { style: BorderStyle.SINGLE, size: 1, color: "E5E7EB" },
        left: { style: BorderStyle.SINGLE, size: 1, color: "E5E7EB" },
        right: { style: BorderStyle.SINGLE, size: 1, color: "E5E7EB" },
      },
      children: [
        new Paragraph({
          children: [new TextRun({ text, size: 20 })],
          alignment: AlignmentType.CENTER,
        }),
      ],
    });

  const tableRows = [
    new TableRow({
      tableHeader: true,
      children: [
        headerCellStyle("#"),
        headerCellStyle("Student No."),
        headerCellStyle("Full Name"),
        headerCellStyle("Status"),
        headerCellStyle("Scanned At"),
      ],
    }),
    ...records.map(
      (r, i) =>
        new TableRow({
          children: [
            dataCell(String(i + 1), i % 2 === 0),
            dataCell(r.student_number, i % 2 === 0),
            dataCell(r.full_name, i % 2 === 0),
            dataCell(r.status.charAt(0).toUpperCase() + r.status.slice(1), i % 2 === 0),
            dataCell(formatTime(r.scanned_at), i % 2 === 0),
          ],
        })
    ),
  ];

  const doc = new Document({
    sections: [
      {
        children: [
          new Paragraph({
            text: `${className} — Attendance Report`,
            heading: HeadingLevel.HEADING_1,
            spacing: { after: 120 },
          }),
          new Paragraph({
            children: [
              new TextRun({ text: "Date: ", bold: true }),
              new TextRun(formatDate(session.attendance_date)),
            ],
            spacing: { after: 60 },
          }),
          new Paragraph({
            children: [
              new TextRun({ text: "Summary:  ", bold: true }),
              new TextRun({ text: `${present} Present`, color: "166534", bold: true }),
              new TextRun("   "),
              new TextRun({ text: `${absent} Absent`, color: "991B1B", bold: true }),
              new TextRun("   "),
              new TextRun(`${records.length} Total`),
            ],
            spacing: { after: 240 },
          }),
          new Table({
            width: { size: 100, type: WidthType.PERCENTAGE },
            rows: tableRows,
          }),
          new Paragraph({
            text: `Generated by Students Attendance System · ${new Date().toLocaleString("en-PH")}`,
            spacing: { before: 240 },
            run: { size: 18, color: "9CA3AF" },
          }),
        ],
      },
    ],
  });

  const blob = await Packer.toBlob(doc);
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `attendance_${className.replace(/\s+/g, "_")}_${session.attendance_date}.docx`;
  a.click();
  URL.revokeObjectURL(url);
}

